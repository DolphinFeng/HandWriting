// 给定一个正整数n,你可以做如下操作:如果n是偶数,则用n/2替换n。如果n是奇数,则可以用n+1
// 或n-1替换n。返回n变为1所需的最小替换次数。示列1:输入:n=8输出:3解释:8->4->2->1
// 示例2:输入:n=7输出:4解释:7->8->4->2->11或7->6->3->2->1示例3:输入:n=4输
// 出:2


function integerReplacement(n) {
    let count = 0;
    while (n !== 1) {
        if (n % 2 === 0) {
            n = n / 2;
        } else if (n === 3 || (n + 1) % 4 === 0) {
            n--;
        } else {
            n++;
        }
        count++;
    }
    return count;
}
