// 爬楼梯: 假设你正在爬楼梯。需要 n 阶你才能到达楼顶。每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢？

let n = 2; // 需要爬的台阶数 有两种方法可以爬到楼顶。1. 1 阶 + 1 阶 2. 2 阶

function climbStairs(n) {
    if (n <= 1) return 1; // 如果台阶数小于等于1，只有一种方法

    let dp = new Array(n + 1); // 创建一个数组来存储每个台阶的方法数
    dp[0] = 1; // 爬0阶有1种方法（不动）
    dp[1] = 1; // 爬1阶有1种方法（一步）

    for (let i = 2; i <= n; i++) {
        dp[i] = dp[i - 1] + dp[i - 2]; // 爬i阶的方法数等于爬i-1阶和爬i-2阶的方法数之和
    }

    return dp[n]; // 返回爬n阶的方法数
}

console.log(climbStairs(n)); // 输出爬2阶的方法数