/**
 * 递归倒计时函数
 * @param {number} t - 倒计时的初始时间（毫秒）
 */
function start (t) {
    // 设置一个定时器，延迟1秒执行
    setTimeout(() => {
        // 每次调用时将时间减少1000毫秒（1秒）
        t = t - 1000
        // 如果时间大于0，继续递归调用start函数
        if (t > 0) {
            // 打印当前剩余时间
            console.log(t);
            // 递归调用start函数，继续倒计时
            start(t)
        }
    }, 1000)
}

// 开始倒计时，从10000毫秒（10秒）开始
start(10000)
